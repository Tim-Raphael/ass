use crate::data::Data;
use rand::seq::SliceRandom;
use rand::thread_rng;
use std::collections::HashMap;

impl Data {
    pub fn gen_random(count: u32) -> Self {
        let mut items = Vec::new();
        let mut items_content_map = HashMap::new();
        for _i in 0..count {
            let random_item = Data::gen_random_item();
            let random_content = Data::gen_random_content();
            items.push(random_item.clone());
            items_content_map.insert(random_item.to_ascii_lowercase(), random_content);
        }
        Data {
            items,
            items_content_map,
        }
    }

    pub fn gen_random_content() -> String {
        format!(
            "{} __ {} __ {}",
            Data::gen_random_description(),
            Data::gen_random_properties(),
            Data::gen_random_image()
        )
    }

    pub fn gen_random_item() -> String {
        let mut rng = thread_rng();
        format!(
            "{} {} ({})",
            *CATEGORIES.choose(&mut rng).unwrap(),
            *COLORS.choose(&mut rng).unwrap(),
            *SIZES.choose(&mut rng).unwrap()
        )
    }

    fn gen_random_description() -> String {
        let mut rng = thread_rng();
        let sentence1 = format!(
            "{} {} {}",
            *BEGINNINGS.choose(&mut rng).unwrap(),
            *MIDDLES.choose(&mut rng).unwrap(),
            *ENDINGS.choose(&mut rng).unwrap()
        );
        let sentence2 = format!(
            "{} {} {}",
            *BEGINNINGS.choose(&mut rng).unwrap(),
            *MIDDLES.choose(&mut rng).unwrap(),
            *ENDINGS.choose(&mut rng).unwrap()
        );
        let sentence3 = format!(
            "{} {} {}",
            *BEGINNINGS.choose(&mut rng).unwrap(),
            *MIDDLES.choose(&mut rng).unwrap(),
            *ENDINGS.choose(&mut rng).unwrap()
        );
        format!("{} {} {}", sentence1, sentence2, sentence3)
    }

    fn gen_random_properties() -> String {
        let mut rng = thread_rng();
        let properties = *PROPERTY_LISTS.choose(&mut rng).unwrap();
        properties.to_string()
    }

    fn gen_random_image() -> String {
        let mut rng = thread_rng();
        let img = *IMAGE_LISTS.choose(&mut rng).unwrap();
        img.to_string()
    }
}

